package com.hybris.mobile.app.commerce.view;


/**
 * Represent different View Type
 */
public class ViewTypeItem {
    private String name;
    private int image;

    public ViewTypeItem(String name, int image) {
        super();
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
