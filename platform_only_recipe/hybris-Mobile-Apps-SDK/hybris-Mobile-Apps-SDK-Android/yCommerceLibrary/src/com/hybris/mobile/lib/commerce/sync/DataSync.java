package com.hybris.mobile.lib.commerce.sync;

public class DataSync {

    private String id;
    private String data;

    public DataSync(String id, String data) {
        this.id = id;
        this.data = data;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
